    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String idBarang = tsd.getIdBarang();
        String idtransaksi = txtIdTransaksi.getText().trim();
        int jumlah = Integer.parseInt(txtJumlah.getText());

        if (idBarang == "") {

        } else if (jumlah == 0) {

        } else if (jDateChooser1.getDate() == null) {

        } else {
            SimpleDateFormat spf = new SimpleDateFormat("yyyy-MM-dd");
            Date tanggal = Date.valueOf(spf.format(jDateChooser1.getDate()));
            int stok = tsd.ShowStok(idBarang);
            int stokTambah = Integer.parseInt(txtJumlah.getText().trim());
            int total = stok + stokTambah;
            String idDetail = tsd.getIdDetail();

            tsd.Save(idtransaksi, idBarang, tanggal, jumlah);
            tsd.UpdateStatusDetailBarang(idDetail);
            tsd.UpdateStok(idBarang, total);

            Refresh();
        }
        
        dispose();
    }//GEN-LAST:event_btnTambahActionPerformed
