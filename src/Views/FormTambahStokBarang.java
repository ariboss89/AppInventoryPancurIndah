    private void cbInvoiceItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbInvoiceItemStateChanged
        // TODO add your handling code here:            
        cbInvoice.addItemListener(new ItemListener() {
            public void itemStateChanged(ItemEvent e) {

                String invoice = cbInvoice.getSelectedItem().toString();
                int a = tsd.ShowNamaBarang(invoice).size();

                for (int i = 0; i < a; i++) {
                    tsd.setIdDetail(tsd.ShowNamaBarang(invoice).get(0));
                    tsd.setIdBarang(tsd.ShowNamaBarang(invoice).get(1));
                    txtNamaBarang.setText(tsd.ShowNamaBarang(invoice).get(2));
                    txtJumlah.setText(tsd.ShowNamaBarang(invoice).get(3));
                }
            }
        }
         );
    }//GEN-LAST:event_cbInvoiceItemStateChanged

