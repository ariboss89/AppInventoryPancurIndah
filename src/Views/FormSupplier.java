    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        idSupplier = txtId.getText().trim();
        nama = txtNama.getText().trim();
        kontak = txtKontak.getText().trim();
        alamat = jTextArea1.getText().trim();
        
        if (txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong !!");
            txtNama.requestFocus();
        } else if (txtKontak.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong !!");
            txtKontak.requestFocus();
        } else if (jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong !!");
            jTextArea1.requestFocus();
        } else {
            sd.Save(idSupplier, nama, kontak, alamat);
            Refresh();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

